    <main>
        <section>
            <h2>Qu'est-ce que la démarche NIRD ?</h2>
            <p>
                La démarche NIRD vise à promouvoir un numérique scolaire plus inclusif, responsable et durable.
                Elle s’inscrit dans le cadre de la forge des communs numériques éducatifs, favorisant le partage
                et la collaboration entre enseignants et établissements.
            </p>
            <ul>
                <li><strong>Numérique inclusif :</strong> faciliter l'accès au numérique pour tous.</li>
                <li><strong>Responsable :</strong> adopter des pratiques respectueuses des données et utiliser des logiciels libres.</li>
                <li><strong>Durable :</strong> limiter l'obsolescence et prolonger la durée de vie du matériel informatique.</li>
            </ul>
        </section>

        <section>
            <h2>Actions concrètes de NIRD</h2>
            <ul>
                <li>Passage progressif à des systèmes d’exploitation libres, comme GNU/Linux.</li>
                <li>Reconditionnement du matériel informatique pour prolonger sa durée de vie.</li>
                <li>Promotion de l'accès équitable pour tous les élèves et leurs familles.</li>
                <li>Utilisation et partage de ressources éducatives libres via la forge des communs éducatifs.</li>
                <li>Intégration d’une stratégie numérique et écologique dans les établissements.</li>
            </ul>
        </section>

        <section>
            <h2>Enjeux et justification</h2>
            <ul>
                <li>Réduire la dépendance à certains systèmes propriétaires et garantir la pérennité.</li>
                <li>Limiter la dépendance technologique et favoriser la souveraineté numérique.</li>
                <li>Assurer un accès numérique équitable à tous les élèves.</li>
                <li>Agir pour l’écologie en réduisant les déchets électroniques.</li>
            </ul>
        </section>

        <section>
            <h2>Portée et statut actuel</h2>
            <ul>
                <li>Soutenu par le ministère de l’Éducation nationale.</li>
                <li>Mis en œuvre dans des établissements pilotes, avec objectif d’extension progressive.</li>
                <li>Favorise la collaboration entre établissements, enseignants et élèves via des communs numériques.</li>
            </ul>
        </section>

        <section>
            <h2>Résumé</h2>
            <p>
                La démarche NIRD transforme le numérique scolaire en un projet éducatif, social et écologique.
                Elle favorise l’inclusion, la responsabilité et la durabilité, tout en donnant aux établissements
                un contrôle sur leur environnement numérique et en sensibilisant les élèves à un usage réfléchi.
            </p>
